module.exports = async function handler(req, res) {
  res.setHeader("Access-Control-Allow-Origin", "*");
  res.setHeader("Access-Control-Allow-Methods", "POST, OPTIONS");
  res.setHeader("Access-Control-Allow-Headers", "Content-Type");

  if (req.method === "OPTIONS") {
    res.status(200).end();
    return;
  }

  if (req.method !== "POST") {
    res.status(405).json({ error: "Yalnızca POST destekleniyor." });
    return;
  }

  const apiKey = process.env.ANTHROPIC_API_KEY;
  if (!apiKey) {
    res.status(500).json({ error: "Sunucu yapılandırılmamış." });
    return;
  }

  try {
    // Vercel body'yi bazen parse etmez — her iki durumu da ele al
    let body = req.body;
    if (typeof body === "string") {
      body = JSON.parse(body);
    }
    if (!body) {
      // stream olarak gelen body'yi manuel oku
      body = await new Promise((resolve, reject) => {
        let data = "";
        req.on("data", chunk => { data += chunk; });
        req.on("end", () => {
          try { resolve(JSON.parse(data)); }
          catch (e) { reject(new Error("JSON parse hatası")); }
        });
        req.on("error", reject);
      });
    }

    const { messages } = body;
    if (!Array.isArray(messages) || messages.length === 0) {
      res.status(400).json({ error: "Geçersiz istek." });
      return;
    }

    const trimmed = messages.slice(-10);

    const apiRes = await fetch("https://api.anthropic.com/v1/messages", {
      method: "POST",
      headers: {
        "Content-Type": "application/json",
        "x-api-key": apiKey,
        "anthropic-version": "2023-06-01"
      },
      body: JSON.stringify({
        model: "claude-sonnet-4-6",
        max_tokens: 1024,
        system: SYSTEM_PROMPT,
        messages: trimmed
      })
    });

    if (!apiRes.ok) {
      let detail = "";
      try { const j = await apiRes.json(); detail = j.error?.message || ""; } catch (e) {}
      res.status(apiRes.status).json({ error: detail || "Anthropic hatası: " + apiRes.status });
      return;
    }

    const data = await apiRes.json();
    const text = (data.content || [])
      .filter(b => b.type === "text")
      .map(b => b.text)
      .join("\n")
      .trim();

    res.status(200).json({ text });

  } catch (err) {
    res.status(500).json({ error: "Hata: " + (err && err.message ? err.message : String(err)) });
  }
};
